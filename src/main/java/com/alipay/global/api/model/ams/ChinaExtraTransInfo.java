package com.alipay.global.api.model.ams;

public class ChinaExtraTransInfo {

    private String businessType;
    private String flightNumber;
    private String departureTime;
    private String hotelName;
    private String checkinTime;
    private String checkoutTime;
    private String admissionNoticeUrl;
    private String totalQuantity;
    private String goodsInfo;
    private String otherBusinessType;

    public String getBusinessType() {
        return businessType;
    }

    public void setBusinessType(String businessType) {
        this.businessType = businessType;
    }

    public String getFlightNumber() {
        return flightNumber;
    }

    public void setFlightNumber(String flightNumber) {
        this.flightNumber = flightNumber;
    }

    public String getDepartureTime() {
        return departureTime;
    }

    public void setDepartureTime(String departureTime) {
        this.departureTime = departureTime;
    }

    public String getHotelName() {
        return hotelName;
    }

    public void setHotelName(String hotelName) {
        this.hotelName = hotelName;
    }

    public String getCheckinTime() {
        return checkinTime;
    }

    public void setCheckinTime(String checkinTime) {
        this.checkinTime = checkinTime;
    }

    public String getCheckoutTime() {
        return checkoutTime;
    }

    public void setCheckoutTime(String checkoutTime) {
        this.checkoutTime = checkoutTime;
    }

    public String getAdmissionNoticeUrl() {
        return admissionNoticeUrl;
    }

    public void setAdmissionNoticeUrl(String admissionNoticeUrl) {
        this.admissionNoticeUrl = admissionNoticeUrl;
    }

    public String getTotalQuantity() {
        return totalQuantity;
    }

    public void setTotalQuantity(String totalQuantity) {
        this.totalQuantity = totalQuantity;
    }

    public String getGoodsInfo() {
        return goodsInfo;
    }

    public void setGoodsInfo(String goodsInfo) {
        this.goodsInfo = goodsInfo;
    }

    public String getOtherBusinessType() {
        return otherBusinessType;
    }

    public void setOtherBusinessType(String otherBusinessType) {
        this.otherBusinessType = otherBusinessType;
    }
}
